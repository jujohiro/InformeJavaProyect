package EstructuiraFor;


public class EstructurasFor {

    public static void main(String[] args) {
         
        int numero = 5;  

        for (int i = 1; i <= 10; i++) {  // Inicializamos i en 1, el ciclo se ejecuta mientras i sea menor o igual a 10, incrementamos i en cada iteración
            System.out.println(numero + " x " + i + " = " + (numero * i));  // Imprimimos la multiplicación del número por i
        }
    }
}
      
    

